var makeNegative = function(num) {
  return num * -1;
};

console.log(makeNegative(-1)); // should return 1
console.log(makeNegative(2)); // should return -2
console.log(makeNegative(9)); // should return -9
console.log(makeNegative(49)); // should return -49
console.log(makeNegative(-20)); // should return 20
console.log(makeNegative(-81)); // should return 81
console.log(makeNegative(100)); // should return -100